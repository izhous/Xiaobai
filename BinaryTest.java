public class BinaryTest {
	public static void main(String[] args) {
		
		//n1 二进制 以0b或者0B开头
		int n1 = 0b1010;
		//n2 10进制 10开头
		int n2 = 1010;
		//n3 8进制,以0开头
		int n3 = 01010;
		//n4 16进制,以0x或者0X开头
		int n4 = 0X10101;
		System.out.println("n1=" + n1);
		System.out.println("n2=" + n2);
		System.out.println("n3=" + n3);
		System.out.println("n4=" + n4);
		
	}
}